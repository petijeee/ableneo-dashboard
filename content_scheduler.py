# ── HTML generation ────────────────────────────────────────────────────────────

def build_article_html(article: dict) -> str:
    return article["content"].strip()


def build_schema(article: dict) -> str:
    schema = {
        "@context": "https://schema.org",
        "@type": "Article",
        "headline": article["title"],
        "author": {"@type": "Organization", "name": "Ableneo"},
        "publisher": {"@type": "Organization", "name": "Ableneo", "url": WP_URL},
    }
    return f'<script type="application/ld+json">\n{json.dumps(schema, ensure_ascii=False, indent=2)}\n</script>\n\n'


# ── WordPress helpers ──────────────────────────────────────────────────────────

def get_auth_header() -> dict:
    token = base64.b64encode(f"{WP_USER}:{WP_PASSWORD}".encode()).decode()
    return {"Authorization": f"Basic {token}"}


def find_existing_post(slug: str) -> int | None:
    resp = requests.get(
        f"{WP_URL}/wp-json/wp/v2/posts",
        params={"slug": slug, "status": "any"},
        headers=get_auth_header(),
        timeout=15,
    )
    resp.raise_for_status()
    posts = resp.json()
    return posts[0]["id"] if posts else None


def get_or_create_category(name: str, slug: str) -> int:
    resp = requests.get(
        f"{WP_URL}/wp-json/wp/v2/categories",
        params={"slug": slug},
        headers=get_auth_header(),
        timeout=15,
    )
    resp.raise_for_status()
    cats = resp.json()
    if cats:
        return cats[0]["id"]

    resp = requests.post(
        f"{WP_URL}/wp-json/wp/v2/categories",
        json={"name": name, "slug": slug},
        headers={**get_auth_header(), "Content-Type": "application/json"},
        timeout=15,
    )
    resp.raise_for_status()
    return resp.json()["id"]


def schedule_post(article: dict, publish_date: datetime | None, category_id: int, dry_run: bool) -> None:
    slug  = article["slug"]
    title = article["title"]
    html  = build_schema(article) + build_article_html(article)

    # None = publish immediately; datetime = schedule for future
    if publish_date is None:
        status   = "publish"
        date_str = None
    else:
        status   = "future"
        date_str = publish_date.strftime("%Y-%m-%dT%H:%M:%S")

    if dry_run:
        label = "NOW" if date_str is None else date_str
        print(f"  [DRY RUN] '{title}' → {label}")
        return

    existing_id = find_existing_post(slug)

    payload = {
        "title":      title,
        "slug":       slug,
        "status":     status,
        "content":    html,
        "categories": [category_id],
    }
    if date_str:
        payload["date"] = date_str
    headers = {**get_auth_header(), "Content-Type": "application/json"}

    if existing_id:
        resp = requests.post(
            f"{WP_URL}/wp-json/wp/v2/posts/{existing_id}",
            json=payload, headers=headers, timeout=30,
        )
    else:
        resp = requests.post(
            f"{WP_URL}/wp-json/wp/v2/posts",
            json=payload, headers=headers, timeout=30,
        )

    if resp.status_code in (200, 201):
        link = resp.json().get("link", "")
        print(f"  ✓  '{title}' → {date_str}  {link}")
    else:
        print(f"  ✗  '{title}' ERROR {resp.status_code}: {resp.text[:200]}", file=sys.stderr)


def show_status() -> None:
    print("Checking scheduled posts…\n")
    resp = requests.get(
        f"{WP_URL}/wp-json/wp/v2/posts",
        params={"status": "future", "per_page": 100},
        headers=get_auth_header(),
        timeout=15,
    )
    resp.raise_for_status()
    posts = resp.json()
    if not posts:
        print("No scheduled posts found.")
        return
    for p in posts:
        print(f"  {p['date']}  {p['title']['rendered']}")


