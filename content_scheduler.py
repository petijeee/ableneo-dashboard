if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Schedule 60 AI articles on ableneo.com")
    parser.add_argument("--dry-run",  action="store_true", help="Preview without publishing")
    parser.add_argument("--status",   action="store_true", help="Show currently scheduled posts")
    args = parser.parse_args()

    if args.status:
        show_status()
        sys.exit(0)

    if not args.dry_run and (not WP_URL or not WP_USER or not WP_PASSWORD):
        print("ERROR: WP_URL, WP_USER, or WP_APP_PASSWORD not set in .env", file=sys.stderr)
        sys.exit(1)

    cat_id = 0 if args.dry_run else get_or_create_category("AI Transformation", CATEGORY_SLUG)

    # Split: first 60 publish immediately, next batch scheduled 2/day from tomorrow
    immediate  = ARTICLES[:60]
    scheduled  = ARTICLES[60:]

    tomorrow = (datetime.now() + timedelta(days=1)).replace(hour=9, minute=0, second=0, microsecond=0)

    print(f"Publishing {len(immediate)} articles immediately…\n")
    for article in immediate:
        schedule_post(article, None, cat_id, dry_run=args.dry_run)

    if scheduled:
        print(f"\nScheduling {len(scheduled)} articles (2/day from {tomorrow.date()})…\n")
        for i, article in enumerate(scheduled):
            day_offset   = i // 2          # 2 articles per day
            hour         = 9 if i % 2 == 0 else 14   # 09:00 and 14:00
            publish_date = (tomorrow + timedelta(days=day_offset)).replace(hour=hour)
            schedule_post(article, publish_date, cat_id, dry_run=args.dry_run)
        last = tomorrow + timedelta(days=(len(scheduled) - 1) // 2)
        print(f"\nLast scheduled article: {last.date()}")

    print(f"\nDone. {len(immediate)} live now, {len(scheduled)} scheduled.")
